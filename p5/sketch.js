  background(0);
  // just line no fill color
  noFill();
  // draw in white
  stroke(255);
  strokeWeight(pen_think);
  // the command to draw by point positions
  beginShape();
  curveVertex(0, height / 2);
  curveVertex(0, height / 2);
  for (let i = 0; i < pos_array_drawing.length; i++) {

    curveVertex(pos_array_drawing[i][0], pos_array_drawing[i][1]);

  }
  curveVertex(width, height / 2);
  curveVertex(width, height / 2);
  endShape();
  // end the command to draw by point positions
